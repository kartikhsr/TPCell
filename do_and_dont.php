              <ul class="do">
                <li>
                  <p class="text1"><strong>Do Your Research: </strong> Learn as
                    much as possible about the company you'll be interviewing
                    with, and know as much about the post. If the company has
                    retail outlets in your area, visit one. If they produce
                    software products, look them over. Talk to people who know
                    the company, explore the business section of the library to
                    get information about the industry, the company and its
                    management. Look over annual and quarterly reports and check
                    the trade publications for the latest information available.
                  </p>
                </li>
                <li>
                  <p class="text1"><strong>Prepare a List of Questions to ask
                      them:</strong> It is natural for you to have some
                    questions about the position for which you are being
                    interviewed. Be ready to ask them. You won't likely need to
                    ask every one of them, since much of the information may be
                    covered in the interview process itself. But you should be
                    prepared to find out the specifics. </p>
                </li>
                <li>
                  <p class="text1"><strong>Dress Professionally:</strong> Make
                    sure your first impression is a good one by looking
                    professional and confident. Even when the corporate culture
                    is more casual, it doesn't hurt to be the best dressed
                    person in the room during an interview. </p>
                </li>
                <li>
                  <p class="text1"> <strong>Arrive Fashionably Early:</strong>
                    You should be at the destination and reported to the
                    receptionist well before the interview. Fifteen minutes
                    early is a good rule of thumb. To make sure you don't arrive
                    at the last minute, go over your travel route in advance,
                    and even drive it once to test the route so you are
                    absolutely confident you know how long it will take. </p>
                </li>
                <li>
                  <p class="text1"><strong>Maintain a Friendly and Professional
                      Demeanor:</strong> Be accessible and open, but always
                    professional. Listen carefully to the questions being asked,
                    and make relevant and concise responses. Stay on course with
                    your answers, and provide specific examples whenever
                    appropriate to reinforce the points you are making. </p>
                </li>
                <li>
                  <p class="text1"> <strong>Ask Permission to Take Notes:</strong>
                    It implies that you are very interested in the position and
                    the company, and it shows clearly that you are well
                    organized, detail oriented and thorough. </p>
                </li>
                <li>
                  <p class="text1"> <strong>Answer Confidently:</strong> Even
                    the toughest questions can be handled with poise and
                    confidence. Don't try to hide weaknesses in your experience
                    or difficult relationships in your work history. Never dwell
                    on the negative, and put a positive, but honest spin on
                    things. </p>
                </li>
              </ul>
              <div class="dividerHeading">
                <h4><span>Things to Avoid</span></h4>
              </div>
              <p class="text1"> Try to avoid giving out too much personal
                information (i.e., financial problems, marital status, children,
                etc.). Avoid questions about salary and benefits until the job
                is offered. Don't talk too much. Bring a few pointed, well
                thought out questions regarding the position (i.e., duties,
                responsibilities, territory size, commission structure,
                potential for advancement). Don't be late. If at all possible,
                be early at least 5-10 minutes prior to the interview. Don't
                brag. It is fine to bring a "brag book" with you to the
                interview that lists your accomplishments. However, try not to
                rely on it. Use it as a guide to show the interviewer what you
                can do for their company. </p>
