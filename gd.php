          <div style="text-align: justify;"><img src="images/gd1.jpg" class="img6">
            Group discussion is an important activity in academic, business and
            administrative spheres. It is a systematic and purposeful
            interactive oral process. Here the exchange of ideas, thoughts and
            feelings take place through oral communication. The exchange of
            ideas takes place in a systematic and structured way. The
            participants sit facing each other almost in a semi-circle and
            express their views on the given topic/issue/problem.Some steps are
            there to make your GD good. </div>
          <ul class="do fr_content" sryle="margin-right:20px;">
            <li> <strong>Subject Knowledge:</strong> Participants must possess
              a thorough understanding of the topic on which they are supposed
              to speak. You must prepare yourself to talk on a wide range of
              subjects. Be abreast of the current events, national and
              international affairs, burning social and economical topics,
              scientific and environmental issues, key newspapers’ controversial
              topics and any experience that may be expected of an educated
              person. As a member of the group, you are expected to contribute
              substantially to the discussion. The originality of your ideas,
              your knowledge and initiative and your approach to the topic or
              case contribute to your success in the group discussion. The best
              way to equip yourself is to read daily newspapers, good magazines,
              national and international journals and also watch new bulletins
              and informative programmes on the television. Internet is the
              greatest boon which provides you with everything you are looking
              for. The World Wide Web is a vast database of current authentic
              materials that present information in multimedia form and reacts
              instantly to a user’s input. The greater your knowledge of the
              subject, the more enthusiastic and confident you will be during
              the discussion. Once you have understood the topic or issue, you
              should be able to generate ideas as well as organize them so that
              you present it well. You will have the ability to analyze facts or
              information in a systematic way. A person putting forward new
              ideas that may work will be accepted as the natural leader of the
              group. The panel will observe the ideas put forward,their
              originality, the depth of analysis and their relevance to the
              topic. </li>
            <li> <strong>Oral Communication Skills:</strong> If subject
              knowledge is important, communication skills is more important as
              without expression, the knowledge is of no use. As the exchange of
              ideas in a group discussion takes place through speech, one of the
              pre-requisites of success in a GD is the ability to
              speak confidently and convincingly. Good communication skills
              include active listening, clarity of though and expression, apt
              language and proper non verbal clues.</li>
            <li> <strong>Listening Skills:</strong> One of the weaknesses of
              most human beings is that we love to listen to our own voice
              rather than listen to others. Listening is as important as
              speaking in a GD, unless you listen, you cannot contribute to the
              stated purpose of communication. It is extremely important to
              listen very carefully, only then you will be able to pick up the
              thread of discussion and continue. Only active participation as a
              listener in a group makes a person a good leader. A leader is
              identified by the panel.</li>
            <li><strong>Clarity of thought and expression:</strong> Clarity is
              the art of making yourself clear to the audience. Only when your
              expressions are clear, you can convince your team and the panel.
              More than words, it is the tone in which they are spoken that
              conveys the message. You should not be too loud or too soft. A
              lively and cheerful voice with appropriate modulations will
              attract the audience. Proper articulation of words, achieved
              through phonetic accuracy is very essential slang, and artificial
              accents are to be avoided. </li>
            <li><strong>Apt Language:</strong> The flow of language must be
              smooth. Use simple language and avoid long winding sentences.
              Appropriateness of language demands that there should be no errors
              of grammar. Do not use unfamiliar phrases and flowery language. Be
              precise. Be polite and courteous.</li>
            <li><strong>Proper non verbal clues</strong>: Non verbal clues
              include eye contact, body movements, gestures and facial
              expressions. The panel very keenly watches the non verbal behavior
              of the team. They generally evaluate the body language cues of the
              team to determine personality factors such as nervousness,
              co-operation, frustration, weakness, insecurity, self confidence,
              defensiveness, etc. A candidate who appears professional is more
              likely to be noticed by the panel. A confident posture,
              appropriate facial expressions and meaningful eye contact with the
              team will create a good expression. </li>
            <li><strong>Team behavior:</strong> Your group behavior is reflected
              in your ability to interact with the other members of the group.
              You must be mature enough to not lose your temper even if you are
              proved wrong. You must be patient and balanced. Your success in a
              GD depends on how well you play the role of initiator, information
              seeker, information giver, procedure facilitator, opinion seeker,
              opinion giver, clarifier, summarizer, social-supporter, tension
              reliever, compromiser, attacker, humorist and dominator. The
              selection panel notes the differences in the amount of
              participation of the members. They observe the silent spectators,
              the ever dominating but not contributing much, member who
              participates actively exhibiting his knowledge and the moderate
              ones. Your ability lies in analyzing the problem well and making
              others to endorse your view. Finally while appreciating others
              point of view, you should effectively present yours without
              contradicting other’s opinions. Your ability in convincing the
              team is your success </li>
          </ul>
