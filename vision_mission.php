                Arrangements for Pre-Placements talks, written tests, group
                discussions, interviews etc are handled by the staff at the
                office in coordination with the respective departments. The
                Placement activities are handled by the Placement Cell, which
                includes final year students from different departments under
                the guidance of Director, Placement. Every Department has a
                T&amp;P Committee, consisting of Chairman of the department, a
                teacher as T&amp;P advisor and 2-3 students volunteers. There is
                one T&amp;P council headed by Vice-Chancellor. </p>
              <p class="fr_content" style="text-align: justify;"> Training and
                Placement Cell keep a liaison with the potential industries and
                provide necessary guidance to the students. The Cell facilitates
                arranging on-campus placements of students in various
                industries, makes arrangements for students to participate in
                off-campus drives organized at various institutions/industries
                and industrial training, an essential component of engineering
                courses is a part of curriculum. The cell provides assistance in
                arranging training of students in concerned industries, which
                helps them in getting placements towards the completion of their
                courses. In this regard the T&amp;P cell has contacted many
                industries for on job training of students and has succeeded in
                arranging on job summer training for the students. The Training
                &amp; Placement Cell also arrange lectures from professionals to
                improve soft skills of the students. The objective of the event
                is to check the students across various disciplines such as
                general awareness, reasoning, communication skills and
                behavioral traits. </p>
