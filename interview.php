              <p class="text1"><strong>Arrive on Time, Relaxed and Prepared for
                  the Interview: </strong> There is no excuse ever for arriving
                late to an interview. Short of a disaster, strive to arrive
                about 15 minutes before your scheduled interview to complete
                additional paperwork and allow yourself time to get settled.
                Arriving a bit early is also a chance to observe the dynamics of
                the workplace. The day before the interview, pack up extra
                copies of your resume or CV and reference list. If you have a
                portfolio or samples of your work, bring those along too.
                Finally, remember to pack several pens and a pad of paper to jot
                notes. Finally, as you get to the offices, shut off your cell
                phone. (And if you were chewing gum, get rid of it.) </p>
            </li>
            <li>
              <p class="text1"><strong>Make Good First Impressions: </strong>A
                cardinal rule of interviewing is to be polite and offer warm
                greetings to everyone you meet from the parking attendant to the
                receptionist to the hiring manager. Employers often are curious
                how job applicants treat staff members and your job offer could
                easily be derailed if you’re rude or arrogant to any of the
                staff. When it’s time for the interview, keep in mind that first
                impressions the impression interviewers get in the first few
                seconds of meeting you can make or break an interview. Make a
                strong first impression by dressing well , arriving early and
                when greeting your interviewer, stand, smile, make eye contact,
                and offer a firm but not bone-crushing handshake. Remember that
                having a positive attitude and expressing enthusiasm for the job
                and employer are vital in the initial stages of the interview;
                studies show that hiring managers make critical decisions about
                job applicants in the first 20 minutes of the interview.</p>
            </li>
            <li>
              <p class="text1"><strong>Dress Code: </strong>Students must be
                formally dressed whenever they participate in any interaction
                with a company. This office reserves the right to refuse
                permission to a student to attend the selection process/PPT, if
                they do not follow the following dress code:<br>
                i. Shirt/T-shirts with collar &amp; sleeves without any printed
                text. <br>
                ii. Trousers[full pant].<br>
                iii. Hair combed.<br>
                iv. Shirt to be tucked in.<br>
                v. Salwar Kameez or Formal Trousers and Shirt for Girl Students
                vii. Polished Sandles/Shoes<br>
                vi. Chappals/flip-flops NOT permitted </p>
            </li>
            <li>
              <p class="text1"><strong>Discipline: </strong><br>
                i. Students are expected to be punctual.<br>
                ii. Students should maintain discipline and show ethical
                behavior in every action they take during the placement process.
                Any student found violating the discipline rules set by the
                company or defaming the University name, will be disallowed from
                the placements for the rest of the academic year.<br>
                iii. Students found cheating or misbehaving in the selection
                process (Test/GD/ Interview) will be disallowed from the
                placements for the rest of the academic year.<br>
                iv. Late comers for Aptitude Test/G.D./Interview will not be
                allowed to appear for the selection process.<br>
                <br>
              </p>
            </li>
            <li>
              <p class="text1"><strong>Remember the Importance of Body Language:
                </strong>While the content of your interview responses is
                paramount, poor body language can be a distraction at best or a
                reason not to hire you at worst. Effective forms of body
                language include smiling, eye contact, solid posture, active
                listening, and nodding. Detrimental forms of body language
                include slouching, looking off in the distance, playing with a
                pen, fidgeting in a chair, brushing back your hair, touching
                your face, chewing gum, or mumbling. Read more about perfecting
                your body language in our article, The Unspoken Secrets of Job
                Interviewing: How Your Nonverbal Presentation and Behaviors
                Impact the Impression You Make.<br>
                <br>
              </p>
            </li>
            <li>
              <p class="text1"><strong>Be Authentic, Upbeat, Focused, Confident,
                  Candid, and Concise: </strong>Once the interview starts, the
                key to success is the quality and delivery of your responses.
                Your goal should always be authenticity, responding truthfully
                to interview questions. At the same time, your goal is to get to
                the next step, so you’ll want to provide focused responses that
                showcase your skills, experience, and fit with the job and the
                employer. Provide solid examples of solutions and
                accomplishments but keep your responses short and to the point.
                By preparing responses to common interview questions (see #2),
                you’ll ideally avoid long, rambling responses that bore
                interviewers. Always attempt to keep your interview responses
                short and to the point. Finally, no matter how much an
                interviewer might bait you, never badmouth a previous employer,
                boss, or co-worker. The interview is about you and making your
                case that you are the ideal candidate for the job. Read about
                more interview mistakes in our article, Avoid These 10 Interview
                Bloopers Critical Jobseeker Mistakes. </p>
            </li>
